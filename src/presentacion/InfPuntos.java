    private void btnConvertirSaldoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConvertirSaldoMouseClicked
        Float saldoFloat;
        String saldo;
        do{
            saldo = JOptionPane.showInputDialog("Ingrese los puntos a convertir en saldo:");
            saldoFloat=Float.parseFloat(saldo);
            if(saldoFloat<=0.0f)
                showMessageDialog(null, "Saldo no válido " , "Error", 0 );
        }while(saldoFloat<=0.0f);
        String mensaje = BLCuenta.converterPuntosCreditoASaldo(codCuentica, saldoFloat);
        Float saldoNuevo;
        if(mensaje == null){
            saldoNuevo = Float.parseFloat(BLCuenta.obtenerSaldo(codCuentica)); 
            showMessageDialog(null, "Su nuevo saldo es:  " + saldoNuevo, "Conversión existosa", 1 );                        
        }else{
            showMessageDialog(null, mensaje, "Error",0);
        }
    }//GEN-LAST:event_btnConvertirSaldoMouseClicked


