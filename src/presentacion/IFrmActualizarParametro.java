    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        codigo = txtCodigo.getText();
        parametro = BLParametro.obtenerParametro(codigo);
        if(parametro != null) {
            if(parametro.getParaCodigo().compareTo("00") != 0) {
                codigo = parametro.getParaCodigo();
                txtValor.setText(parametro.getParaValor());
                txtEstado.setText(parametro.getParaEstado());
                txtCodigo.setEnabled(false);
                btnBuscar.setEnabled(false);
                txtNueva.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "El código no existe", "Resultado", 1);
                limpiar();
                activar(false);
                btnNuevo.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "El código no es valido", "Resultado", 0);
            limpiar();
            activar(false);
            btnNuevo.requestFocus();

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

