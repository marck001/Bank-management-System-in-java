    private void txtNuevaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNuevaKeyTyped
       char c=evt.getKeyChar();
       if(((c<'0')||(c>'9')) && (c != KeyEvent.VK_BACK_SPACE)
                && (c!= '.' || txtNueva.getText().contains(".")))
            evt.consume();
    }//GEN-LAST:event_txtNuevaKeyTyped
        private void activar(boolean estado) {
        txtCodigo.setEnabled(estado);
        txtValor.setEnabled(estado);
        txtEstado.setEnabled(estado);
        cbxNueva.setEnabled(estado);
        txtNueva.setEnabled(estado);
        btnActualizar.setEnabled(estado);
        btnNuevo.setEnabled(!estado);
    }
    
    private void limpiar() {
        txtCodigo.setText(null);
        txtValor.setText(null);
        cbxNueva.setSelectedIndex(0);
        
    }
    private void llenarTabla() {
        modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(columnas);
        iterador = lista.iterator();
        while (iterador.hasNext()) {
            parametro = iterador.next();
            fila[0] = parametro.getParaCodigo();
            fila[1] = parametro.getParaDescripcion();
            fila[2] = parametro.getParaValor();
            fila[3] = parametro.getParaEstado();
            modelo.addRow(fila);
        }
        tblParametro.setModel(modelo);
    }

