    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        codigo = txtCodigo.getText();
        cliente = BLCliente.obtenerCliente(codigo);
        if(cliente != null) {
            if(cliente.getCodigo().compareTo("00000") != 0) {
                codigo = cliente.getCodigo();
                
                txtNombre.setText(cliente.getNombre()+" "+ cliente.getApellidoPaterno()+ " "+ " " +cliente.getApellidoMaterno());
                txtDni.setText(cliente.getDni());
                txtCiudad.setText(cliente.getCiudad());
                txtDireccion.setText(cliente.getDireccion());
                txtTelefono.setText(cliente.getTelefono());
                txtMail.setText(cliente.getEmail());
                txtCodigo.setEnabled(false);
                
                txtCodigo.requestFocus();
                
                txtCiudad.requestFocus();
                txtDni.setEnabled(false);
                txtNombre.setEnabled(false);
                txtCiudad.setEnabled(false);
                txtDireccion.setEnabled(false);
                txtTelefono.setEnabled(false);
                btnBuscar.setEnabled(false);
                
                
            } else {
                JOptionPane.showMessageDialog(this, "El código ingresado no existe", "Resultado", 1);
                limpiar();
                activar(false);
                btnNuevo.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "El código ingresado no es valido", "Resultado", 0);
            limpiar();
            activar(false);
            btnNuevo.requestFocus();
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

