    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String respuesta;
        codigosuc = txtCodigo.getText();
        nombresuc = txtNombre1.getText();
        ciudadsuc = txtCiudad1.getText();
        direccionsuc = txtDireccion1.getText();
        cuentasuc = Integer.parseInt(txtcuentasuc1.getText());
        tiposuc = txtTipo1.getText();

        respuesta = BLSucursal.actualizarSucursal(codigosuc, nombresuc, ciudadsuc, direccionsuc, cuentasuc, tiposuc);
        JOptionPane.showMessageDialog(this, respuesta, "Resultado", JOptionPane.INFORMATION_MESSAGE);
        activar(false);
        limpiar();
        btnNuevo.requestFocus();
    }//GEN-LAST:event_btnActualizarActionPerformed

