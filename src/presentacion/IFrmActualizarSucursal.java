    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)))
        evt.consume();
    }//GEN-LAST:event_txtCodigoKeyTyped

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
          String codigo = txtCodigo.getText();
    Sucursal sucursal = BLSucursal.obtenerSucursal(codigo);
    if (sucursal != null) {
        txtNombre1.setText(sucursal.getNombre());
        txtCiudad1.setText(sucursal.getCiudad());
        txtDireccion1.setText(sucursal.getDireccion());
        txtcuentasuc1.setText(String.valueOf(sucursal.getContCuenta()));
        txtTipo1.setText(sucursal instanceof SucursalDepartamental ? "Departamental" : "Provincial");

        txtCodigo.setEnabled(false);
        btnBuscar.setEnabled(false);
        txtCiudad.setEnabled(false);
        txtDireccion.setEnabled(false);
        txtNombre.setEnabled(false);
        txtcuentasuc.setEnabled(false);
        txtTipo.setEnabled(false);
    } else {
        JOptionPane.showMessageDialog(this, "El cÃ³digo no existe", "Resultado", JOptionPane.WARNING_MESSAGE);
        limpiar();
        activar(false);
        txtCodigo.requestFocus();
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

